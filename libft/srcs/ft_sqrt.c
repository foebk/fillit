#include "libft.h"

int	ft_sqrt(int a, int b)
{
	int	i;

	i = 0;
	while ((i != 46341) && (i * i < a))
	{
		if (i * i == a)
			return (i);
		i++;
	}
	if (b == 1)
		return (i);
	return (0);
}
